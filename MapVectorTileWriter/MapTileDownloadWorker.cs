        public void DownloadMapTile()
        {
            try
            {
                string url = MapType.GetInstance().getTileURL(mapTileIndex.MapType, mapTileIndex.XIndex,
                                                              mapTileIndex.YIndex, mapTileIndex.ZoomLevel);
                localMapTileFileReader.GetImage(mapTileIndex.MapType, mapTileIndex.XIndex,
                                                              mapTileIndex.YIndex,17- mapTileIndex.ZoomLevel);
                byte[] pngData = localMapTileFileReader.ImageArray;

                if(pngData==null)
                {
                    pngData = ImageNoavaiableArray;
                }
                mapTileDownloadManager.AddToImageCache(mapTileIndex, pngData);
                Console.WriteLine("downloaded +"+ url);
                mapTileDownloadManager.DownloadedCount += 1;
                mapTileDownloadManager.AddMessage("downloaded :"+ mapTileDownloadManager.DownloadedCount+"(X=" + mapTileIndex.XIndex + ",Y=" + mapTileIndex.YIndex + ",Z=" + (17-mapTileIndex.ZoomLevel)+")");

            }
            catch (Exception e)
            {
                mapTileDownloadManager.AddToRetryList(mapTileIndex);
            }


